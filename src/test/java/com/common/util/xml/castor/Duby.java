package com.common.util.xml.castor;


public class Duby {
	public String duby;
	public String dubyAge;
	
	
	public Duby(String duby, String dubyAge) {
		super();
		this.duby = duby;
		this.dubyAge = dubyAge;
	}

	public String getDubyAge() {
		return dubyAge;
	}

	public void setDubyAge(String dubyAge) {
		this.dubyAge = dubyAge;
	}

	public Duby() {
		super();
	}

	public Duby(String duby) {
		super();
		this.duby = duby;
	}

	public String getDuby() {
		return duby;
	}

	public void setDuby(String duby) {
		this.duby = duby;
	}
	
}
